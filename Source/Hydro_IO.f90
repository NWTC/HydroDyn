   END DO
      
   
   !-------------------------------------------------------------------------------------------------
   ! Initialize all output channels to zero so that we can avoid doing resetting them to zero at every time step.
   !-------------------------------------------------------------------------------------------------
   HDOut_Data%AllOuts = 0.0 
   
   
   RETURN
