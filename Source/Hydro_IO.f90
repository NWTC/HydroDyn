SUBROUTINE HDOut_CalcOutput ( CurrentTime, HDOut_Data, FP_Data, Wave_Data, ErrStat )
! This subroutine calculates all the output channels available in HydroDyn and stores them
! in the module's internal data structure HDOut_Data%AllOuts(:)
!----------------------------------------------------------------------------------------------------

   USE                                 FloatingPlatform 
   USE                                 Waves

      ! Passed variables
      
   REAL(ReKi),              INTENT(IN   ) :: CurrentTime                               ! The current time in the calculation
   TYPE(HD_OutputDataType), INTENT(INOUT) :: HDOut_Data                                ! This module's internal data
   TYPE(FltPtfm_DataType),  INTENT(IN   ) :: FP_Data                                   ! The floating platform module's internal data
   TYPE(Waves_DataType),    INTENT(INOUT) :: Wave_Data                                 ! The waves module's internal data
   INTEGER,                 INTENT(  OUT) :: ErrStat                                   ! a non-zero value indicates an error occurred           

      
      ! Local variables
      
   REAL(ReKi)                             :: TeAng                                     ! temporary storage for tension angle
   REAL(ReKi)                             :: Te                                        ! temporary storage for tension
   
   INTEGER                                :: I                                         ! Generic loop counter      
   INTEGER                                :: Nd                                        ! Generic loop counter      
   INTEGER                                :: NumLines                                  ! stores FP_Data's NumLines
   INTEGER                                :: LineNodes                                 ! stores FP_Data's LineNodes for a particular line


   !-------------------------------------------------------------------------------------------------      
   ! Initialize some values
   !-------------------------------------------------------------------------------------------------      
   ErrStat = 0
   NumLines = FP_GetNumLines(FP_Data)      

   !-------------------------------------------------------------------------------------------------      
   ! Populate the AllOuts Array
   !-------------------------------------------------------------------------------------------------      
   
   !.................................................................................................
   ! Time
   !.................................................................................................
   HDOut_Data%AllOuts( Time ) = CurrentTime
      
   !.................................................................................................
   ! Mooring Line Loads:
   !.................................................................................................

   DO I = 1,NumLines
   
         ! At the fairlead
         
      CALL FairleadTension ( I, Te, TeAng, FP_Data, ErrStat )
      
      HDOut_Data%AllOuts( FairTen(I) ) = Te   *0.001   ! Convert to kN
      HDOut_Data%AllOuts( FairAng(I) ) = TeAng*R2D     ! Convert to degrees
            
         ! At the anchor
         
      CALL AnchorTension   ( I, Te, TeAng, FP_Data, ErrStat )
       
      HDOut_Data%AllOuts( AnchTen(I) ) = Te   *0.001   ! Convert to kN
      HDOut_Data%AllOuts( AnchAng(I) ) = TeAng*R2D     ! Convert to degrees
   
   
         ! At the nodes
         
      LineNodes = FP_GetNumLineNodes ( FP_Data, I, ErrStat )
      IF ( ErrStat /= 0 ) RETURN
      
      DO Nd = 1,LineNodes
         
         HDOut_Data%AllOuts( LnNdTen(I,Nd) ) = LineTension  ( I, Nd,    FP_Data, ErrStat ) * 0.001   ! Convert to kN  
         IF ( ErrStat /= 0 ) RETURN
                
         HDOut_Data%AllOuts( LnNdxi( I,Nd) ) = LinePosition ( I, Nd, 1, FP_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( LnNdyi( I,Nd) ) = LinePosition ( I, Nd, 2, FP_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( LnNdzi( I,Nd) ) = LinePosition ( I, Nd, 3, FP_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN
         
      END DO !Nd
   
   END DO !I
                
                
   !.................................................................................................
   ! Wave Motions:
   !.................................................................................................

      ! Wave Elevation

   DO I = 1, HDOut_Data%NWaveElevOut

      HDOut_Data%AllOuts( WaveElev(I) ) = WaveElevation ( I, CurrentTime, Wave_Data, ErrStat ) !we just use the first NWaveElev points as output
      IF ( ErrStat /= 0 ) RETURN
      
   END DO ! I 


      ! Wave Velocity and Accelerations

   DO I = 1, 81


      IF ( HDOut_Data%WaveKinNd(I) > 0 ) THEN

         HDOut_Data%AllOuts( MNFVxi(I) ) = WaveVelocity       ( HDOut_Data%WaveKinNd(I), 1, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( MNFVyi(I) ) = WaveVelocity       ( HDOut_Data%WaveKinNd(I), 2, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( MNFVzi(I) ) = WaveVelocity       ( HDOut_Data%WaveKinNd(I), 3, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( MNFAxi(I) ) = WaveAcceleration   ( HDOut_Data%WaveKinNd(I), 1, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( MNFAyi(I) ) = WaveAcceleration   ( HDOut_Data%WaveKinNd(I), 2, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN

         HDOut_Data%AllOuts( MNFAzi(I) ) = WaveAcceleration   ( HDOut_Data%WaveKinNd(I), 3, CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN
         
!jbj: start of proposed change v1.00.00b-jbj
         HDOut_Data%AllOuts( MNDynP(I) ) = WaveDynamicPressure( HDOut_Data%WaveKinNd(I),    CurrentTime, Wave_Data, ErrStat )
         IF ( ErrStat /= 0 ) RETURN
!jbj: end of proposed change v1.00.00b-jbj
         
         
      END IF         

   END DO
                
            
   RETURN                           

END SUBROUTINE HDOut_CalcOutput
