SUBROUTINE HDOut_Terminate ( HDOut_Data, ErrStat )
! This function cleans up after running the HydroDyn output module. It closes the output file,
! releases memory, and resets the number of outputs requested to 0.
!----------------------------------------------------------------------------------------------------

         ! Passed variables

   TYPE(HD_OutputDataType),INTENT(INOUT) :: HDOut_Data                               ! data for this instance of the module
   INTEGER,                INTENT(  OUT) :: ErrStat                                  ! non-zero return value indicates an error occurred

!      ! Internal variables
!   INTEGER                               :: I
   LOGICAL                               :: Err


   !-------------------------------------------------------------------------------------------------
   ! Initialize error information
   !-------------------------------------------------------------------------------------------------
   ErrStat = 0
   Err     = .FALSE.

   !-------------------------------------------------------------------------------------------------
   ! Close our output file
   !-------------------------------------------------------------------------------------------------
   CLOSE( HDOut_Data%UnOutFile, IOSTAT = ErrStat )
   IF ( ErrStat /= 0 ) Err = .TRUE.

   !-------------------------------------------------------------------------------------------------
   ! Deallocate arrays
   !-------------------------------------------------------------------------------------------------
   IF ( ALLOCATED( HDOut_Data%OutParam ) ) DEALLOCATE ( HDOut_Data%OutParam, STAT=ErrStat )
   IF ( ErrStat /= 0 ) Err = .TRUE.
     
   !-------------------------------------------------------------------------------------------------
   ! Reset number of outputs
   !-------------------------------------------------------------------------------------------------
   HDOut_Data%NumOuts   =  0
   HDOut_Data%UnOutFile = -1
   
   HDOut_Data%WaveKinNd = -1        ! set this array to "invalid"

   !-------------------------------------------------------------------------------------------------
   ! Make sure ErrStat is non-zero if an error occurred
   !-------------------------------------------------------------------------------------------------
   IF ( Err ) ErrStat = 1
   
   RETURN

END SUBROUTINE HDOut_Terminate
