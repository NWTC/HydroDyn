SUBROUTINE HDOut_WriteOutputs( HDOut_Data, ErrStat )
! This subroutine writes the data stored in AllOuts (and indexed in OutParam) to the file
! opened in HDOut_Init()
!---------------------------------------------------------------------------------------------------- 

      ! Passed variables     
   TYPE(HD_OutputDataType), INTENT(INOUT) :: HDOut_Data                                ! This module's internal data
   INTEGER,                 INTENT(  OUT) :: ErrStat                                   ! a non-zero value indicates an error occurred           

      ! Local variables
   REAL(ReKi)                             :: OutData (0:HDOut_Data%NumOuts)            ! an output array
   INTEGER                                :: I                                         ! Generic loop counter
   CHARACTER(200)                         :: Frmt                                      ! a string to hold a format statement
   

  
      ! Initialize ErrStat and determine if it makes any sense to write output
      
   IF ( .NOT. ALLOCATED( HDOut_Data%OutParam ) .OR. HDOut_Data%UnOutFile < 0 )  THEN
      ErrStat = -1
      RETURN
   ELSE
      ErrStat = 0
   END IF


         ! Put the output data in the OutData array
   
   DO I = 0,HDOut_Data%NumOuts

      OutData(I) = HDOut_Data%OutParam(I)%SignM * HDOut_Data%AllOuts( HDOut_Data%OutParam(I)%Indx )
      
   END DO      


      ! Write the output parameters to the file
      
   Frmt = '(F8.3,'//TRIM(Int2LStr(HDOut_Data%NumOuts))//'(:,A,'//TRIM( HDOut_Data%OutFmt )//'))'

   WRITE(HDOut_Data%UnOutFile,Frmt)  OutData(0), ( HDOut_Data%Delim, OutData(I), I=1,HDOut_Data%NumOuts )

   
   RETURN


END SUBROUTINE HDOut_WriteOutputs
