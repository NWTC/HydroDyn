  IF( ALLOCATED(Db_WAMIT_Buf) )  DEALLOCATE(Db_WAMIT_Buf)
  IF( ALLOCATED(Int_WAMIT_Buf) ) DEALLOCATE(Int_WAMIT_Buf)
