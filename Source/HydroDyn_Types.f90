  ENDIF
  IF(ALLOCATED(Db_WAMIT_Buf)) THEN
    IF ( .NOT. OnlySize ) DbKiBuf( Db_Xferred:Db_Xferred+SIZE(Db_WAMIT_Buf)-1 ) = Db_WAMIT_Buf
